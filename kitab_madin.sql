DELIMITER ;;
CREATE DEFINER=`root`@`localhost` PROCEDURE `spHitungKebutuhanKitab`(IN `_PID` varchar(50))
BEGIN
SELECT count(tb_santri.NIS) as jumlah
FROM
tb_santri
INNER JOIN tb_paket ON tb_paket._idPaket = tb_santri.idPaket
INNER JOIN tbpaketkitab ON tb_paket._idPaket = tbpaketkitab.idPaket
INNER JOIN tb_matpel ON tbpaketkitab.PID = tb_matpel.PID
WHERE tb_matpel.PID = _PID;
END
;;
DELIMITER ;

